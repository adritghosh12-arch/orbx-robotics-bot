import React, { useState } from 'react';
import Link from 'next/link';
import dynamic from 'next/dynamic';
import { motion } from 'framer-motion';
import styles from '../styles/Cad.module.css';

// Load Three.js viewer only on client side (no SSR)
const CADViewer = dynamic(() => import('../components/CADViewer'), { ssr: false });

// Import STL export function dynamically
let exportSTL;
if (typeof window !== 'undefined') {
  import('../components/CADViewer').then((mod) => {
    exportSTL = mod.exportSTL;
  });
}

export default function Cad() {
  const [description, setDescription] = useState('');
  const [geometry, setGeometry] = useState(null);
  const [assemblyId, setAssemblyId] = useState(null);
  const [loading, setLoading] = useState(false);
  const [error, setError] = useState('');
  const [showHoles, setShowHoles] = useState(true);
  const [modelInfo, setModelInfo] = useState('');

  const examplePrompts = [
    'A mecanum drivetrain fully detailed with motors and wheels',
    'A motor mount plate for a REV HD Hex motor with 4 M3 mounting holes and a center shaft bore',
    'A servo bracket that mounts to goBILDA channel with screw holes',
    'A bearing block for an 8mm shaft with two bearing holders and M3 mounting holes on the base',
    'A claw finger 80mm long with a pivot hole at one end and curved grip surface',
  ];

  const handleGenerate = async (e) => {
    e.preventDefault();
    if (!description.trim()) return;

    setLoading(true);
    setError('');
    setGeometry(null);
    setAssemblyId(null);
    setModelInfo('');

    try {
      const response = await fetch('/api/cad', {
        method: 'POST',
        headers: { 'Content-Type': 'application/json' },
        body: JSON.stringify({ description: description.trim() }),
      });

      const data = await response.json();

      if (!response.ok || !data.success) {
        setError(data.error || 'Failed to generate CAD model');
      } else if (data.useLibrary) {
        // Pre-built assembly from part library
        setAssemblyId(data.assemblyId);
        setModelInfo('Part Library (real FTC hardware)');
      } else {
        setGeometry(data.geometry);
        setModelInfo(`Generated by ${data.model}`);
      }
    } catch (err) {
      setError('Network error. Please try again.');
    } finally {
      setLoading(false);
    }
  };

  const handleDownloadSTL = () => {
    if (!geometry || !exportSTL) return;
    const stl = exportSTL(geometry);
    if (!stl) return;
    const blob = new Blob([stl], { type: 'application/sla' });
    const url = URL.createObjectURL(blob);
    const a = document.createElement('a');
    a.href = url;
    const name = (geometry.name || 'part').toLowerCase().replace(/[^a-z0-9]/g, '_');
    a.download = `${name}.stl`;
    document.body.appendChild(a);
    a.click();
    document.body.removeChild(a);
    URL.revokeObjectURL(url);
  };

  const handleDownloadJSON = () => {
    if (!geometry) return;
    const blob = new Blob([JSON.stringify(geometry, null, 2)], { type: 'application/json' });
    const url = URL.createObjectURL(blob);
    const a = document.createElement('a');
    a.href = url;
    const name = (geometry.name || 'part').toLowerCase().replace(/[^a-z0-9]/g, '_');
    a.download = `${name}.json`;
    document.body.appendChild(a);
    a.click();
    document.body.removeChild(a);
    URL.revokeObjectURL(url);
  };

  return (
    <div className={styles.container}>
      {/* Navigation */}
      <nav className={styles.navbar}>
        <div className={styles.navContent}>
          <h1 className={styles.logo}>🛸 Orbx AI</h1>
          <div className={styles.navLinks}>
            <Link href="/">Home</Link>
            <Link href="/chat">Chat</Link>
            <Link href="/cad">CAD</Link>
            <Link href="/forum">Forum</Link>
          </div>
        </div>
      </nav>

      {/* Header */}
      <header className={styles.header}>
        <motion.div
          initial={{ opacity: 0, y: -20 }}
          animate={{ opacity: 1, y: 0 }}
          transition={{ duration: 0.5 }}
        >
          <h2>🔧 CAD Generator</h2>
          <p>Describe an FTC robot part → get a 3D model you can rotate, zoom, and export as STL</p>
        </motion.div>
      </header>

      <main className={styles.main}>
        <div className={styles.layout}>
          {/* Left: Input */}
          <motion.section
            className={styles.inputSection}
            initial={{ opacity: 0, x: -20 }}
            animate={{ opacity: 1, x: 0 }}
            transition={{ delay: 0.2 }}
          >
            <form onSubmit={handleGenerate}>
              <label className={styles.label} htmlFor="description">
                Describe your part:
              </label>
              <textarea
                id="description"
                className={styles.textarea}
                value={description}
                onChange={(e) => setDescription(e.target.value)}
                placeholder="e.g., A motor mount plate for a REV HD Hex motor with M3 mounting holes..."
                rows={5}
                maxLength={2000}
                disabled={loading}
              />
              <div className={styles.charCount}>{description.length}/2000</div>
              <button
                type="submit"
                className={styles.generateBtn}
                disabled={loading || !description.trim()}
              >
                {loading ? '⏳ Generating 3D Model...' : '🚀 Generate 3D Model'}
              </button>
            </form>

            {/* Examples */}
            <div className={styles.examples}>
              <p className={styles.examplesTitle}>Try an example:</p>
              {examplePrompts.map((prompt, i) => (
                <button
                  key={i}
                  className={styles.exampleBtn}
                  onClick={() => setDescription(prompt)}
                  disabled={loading}
                >
                  {prompt.slice(0, 55)}...
                </button>
              ))}
            </div>
          </motion.section>

          {/* Right: 3D Viewer */}
          <motion.section
            className={styles.viewerSection}
            initial={{ opacity: 0, x: 20 }}
            animate={{ opacity: 1, x: 0 }}
            transition={{ delay: 0.3 }}
          >
            <div className={styles.viewerContainer}>
              {(geometry || assemblyId) ? (
                <CADViewer geometry={geometry} assemblyId={assemblyId} showHoles={showHoles} />
              ) : (
                <div className={styles.viewerPlaceholder}>
                  <p>🎨</p>
                  <p>Your 3D model will appear here</p>
                  <p className={styles.placeholderSub}>
                    Describe a part and click Generate
                  </p>
                </div>
              )}
            </div>

            {/* Controls below viewer */}
            {(geometry || assemblyId) && (
              <div className={styles.viewerControls}>
                <div className={styles.controlsLeft}>
                  <span className={styles.partName}>
                    {assemblyId ? 'Mecanum Drivetrain Assembly' : (geometry?.name || 'Generated Part')}
                  </span>
                  <span className={styles.partCount}>
                    {modelInfo}
                  </span>
                  {geometry && (
                    <label className={styles.toggleLabel}>
                      <input
                        type="checkbox"
                        checked={showHoles}
                        onChange={(e) => setShowHoles(e.target.checked)}
                      />
                      Show holes
                    </label>
                  )}
                </div>
                <div className={styles.controlsRight}>
                  {geometry && (
                    <>
                      <button onClick={handleDownloadSTL} className={styles.actionBtn}>
                        ⬇️ Download STL
                      </button>
                      <button onClick={handleDownloadJSON} className={styles.actionBtn}>
                        📄 Download JSON
                      </button>
                    </>
                  )}
                </div>
              </div>
            )}

            {/* Viewer instructions */}
            {(geometry || assemblyId) && (
              <div className={styles.viewerHelp}>
                🖱️ Left-drag to rotate • Scroll to zoom • Right-drag to pan
              </div>
            )}
          </motion.section>
        </div>

        {/* Error */}
        {error && (
          <motion.div
            className={styles.error}
            initial={{ opacity: 0 }}
            animate={{ opacity: 1 }}
          >
            ❌ {error}
          </motion.div>
        )}
      </main>
    </div>
  );
}
